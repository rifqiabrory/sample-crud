package com.kantin.entity;

public class KantinRekapEntity {

	private int id_rekap;
	private int id_bulan;
	private int total_transaksi;

	public int getId_rekap() {
		return id_rekap;
	}

	public void setId_rekap(int id_rekap) {
		this.id_rekap = id_rekap;
	}

	public int getId_bulan() {
		return id_bulan;
	}

	public void setId_bulan(int id_bulan) {
		this.id_bulan = id_bulan;
	}

	public int getTotal_transaksi() {
		return total_transaksi;
	}

	public void setTotal_transaksi(int total_transaksi) {
		this.total_transaksi = total_transaksi;
	}

}
